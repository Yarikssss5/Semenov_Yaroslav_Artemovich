#!/bin/bash

# Проверяем, передан ли аргумент
if [ $# -eq 0 ]; then
    echo "Использование: $0 <URL>"
    echo "Пример: $0 https://example.com"
    exit 1
fi

URL=$1

# Используем curl для получения HTTP-статуса
HTTP_STATUS=$(curl -s -o /dev/null -w "%{http_code}" "$URL")

echo "URL: $URL"
echo "HTTP статус: $HTTP_STATUS"

# Дополнительная информация о статусе
case $HTTP_STATUS in
    200)
        echo "Статус: OK - Успешный запрос"
        ;;
    301|302)
        echo "Статус: Перенаправление"
        ;;
    400)
        echo "Статус: Плохой запрос"
        ;;
    401)
        echo "Статус: Не авторизован"
        ;;
    403)
        echo "Статус: Доступ запрещен"
        ;;
    404)
        echo "Статус: Не найдено"
        ;;
    500)
        echo "Статус: Внутренняя ошибка сервера"
        ;;
    502)
        echo "Статус: Плохой шлюз"
        ;;
    503)
        echo "Статус: Сервис недоступен"
        ;;
    *)
        echo "Статус: Неизвестный код"
        ;;
esac
